// ──────────────────────────────────────────────────────────────────────────────
// Основная функция
// ──────────────────────────────────────────────────────────────────────────────

async function main() {
  console.log('🌱 Запуск сидирования шаблонов DMED...\n')

  // Найти первого SUPERADMIN, иначе первого ADMIN
  const admin =
    (await prisma.user.findFirst({
      where: { role: 'SUPERADMIN' },
      select: { id: true, name: true, email: true },
    })) ??
    (await prisma.user.findFirst({
      where: { role: 'ADMIN' },
      select: { id: true, name: true, email: true },
    }))

  if (!admin) {
    console.error('❌ Ошибка: в базе не найден ни один SUPERADMIN или ADMIN.')
    console.error('   Создайте администратора перед запуском seed-templates.')
    process.exit(1)
  }

  console.log(`👤 Шаблоны будут созданы от имени: ${admin.name ?? admin.email} (${admin.id})\n`)

  let created = 0
  let skipped = 0

  for (const template of DMED_TEMPLATES) {
    const existing = await prisma.letterTemplate.findFirst({
      where: { name: template.name },
      select: { id: true },
    })

    if (existing) {
      console.log(`  ⏭  Пропуск (уже существует): "${template.name}"`)
      skipped++
      continue
    }

    await prisma.letterTemplate.create({
      data: {
        name: template.name,
        subject: template.subject,
        body: template.body,
        category: template.category,
        variables: template.variables,
        isPublic: true,
        usageCount: 0,
        createdById: admin.id,
      },
    })

    console.log(`  ✓ Создан: "${template.name}"`)
    created++
  }

  console.log(
    `\n✅ Готово: создано ${created}, пропущено ${skipped} из ${DMED_TEMPLATES.length} шаблонов.`
  )
}

main()
  .catch((err) => {
    console.error('❌ Ошибка:', err)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
